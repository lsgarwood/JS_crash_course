//Increment and decrement operators

var number = 0;
//postfix
console.log(number++); //return the number then increment
console.log(number);
console.log(number--); //return the number then increment
console.log(number);
//prefix
var numberTwo = 0;
console.log(++numberTwo); // increment then return number
console.log(--numberTwo); // decrement then return the number
//for loop
for (var i = 0; i<=10; i++) { //return the number then increment
    console.log(i);
}